package loops;

public class Example1 {

	public static void main(String[] args) {
		
		for(int i=0;i<5;i++) {
			System.out.println("Hello Everyone");
		}
        for(int i=10;i>0;i--) {
        	System.out.println(i);
        }
        for(char c1='A';c1<='Z';c1++) {
        	System.out.println(c1);
        }
}
	    
}

